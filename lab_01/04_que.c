#include <stdio.h>

int main()
{
   // displaying average marks of 5 students

   // declaring the variables
   int a = 80;
   int b = 85;
   int c = 70;
   int d = 75;
   int e = 60;
   int average;

   // calculating average
   average = (a + b + c + d + e) * 0.2;

   // printing the avarage
   printf("average marks of 5 students is as follow: = %d\n", average);

   return 0;

   // completed
}
